package com.revature;

public class Paw {
	private int numToes;
	private String toeBeanColor;
	
	public Paw(int numberOfToes, String toeColor) {
		this.numToes = numberOfToes;
		this.toeBeanColor = toeColor;
	}
	
	public int getNumToes() {
		return numToes;
	}

	public String getToeBeanColor() {
		return toeBeanColor;
	}
	
	
}
